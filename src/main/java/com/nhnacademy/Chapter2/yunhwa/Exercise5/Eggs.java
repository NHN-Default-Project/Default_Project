package com.nhnacademy.Chapter2.yunhwa.Exercise5;

public class Eggs {
    private int bundle;
    private int leftEggs;

    public Eggs(int bundle, int leftEggs) {
        this.bundle = bundle;
        this.leftEggs = leftEggs;
    }

    public int getBundle() {
        return this.bundle;
    }

    public int getLeftEggs() {
        return this.leftEggs;
    }
}
